# მომხმარებლისგან სიგრძისა და სიგანის მიღება
length = float(input("შეიყვანეთ ოთკუთხედის სიგრძე: "))
width = float(input("შეიყვანეთ ოთკუთხედის სიგანე: "))

# ფართობის და პერიმეტრის გამოთვლა
area = length * width
perimeter = 2 * (length + width)

print("ოთკუთხედის ფართობია: {area} კვ. მეტრი")
print("ოთკუთხედის პერიმეტრი არის: {perimeter} მეტრი")

