distance_cm = distance_km * 100000
distance_mm = distance_km * 1000000

print("მანძილი მეტრებში: {distance_m} მ")
print("მანძილი სანტიმეტრებში: {distance_cm} სმ")
print("მანძილი მილიმეტრებში: {distance_mm} მმ")

